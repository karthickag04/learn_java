public class JavaArrayWithFOR01a {
    public static void main(String[] args){

        String[] StudentNames={"Ram", "Ravi", "Rajesh"};


        /* 
        
        
        for (Starting index value; condition to check ; increment)  
            {
            type the statement to execute 
            }
          
          
        */



        for(int index=0; index < StudentNames.length; index++){
            System.out.println(StudentNames[index]);
            
        }


      




    }
}
