// Below is the document that covers Java tokens with only the essential content.

// ------------------------------------------------------------
//                          Java Tokens

// 1. Keywords
// -------------
// Reserved words predefined by Java (Java SE 17+):

// abstract, assert, boolean, break, byte  
// case, catch, char, class, const*, continue  
// default, do, double, else, enum, extends, final, finally, float, for, goto*  
// if, implements, import, instanceof, int, interface, long, native, new, package, private, protected, public  
// return, short, static, strictfp, super, switch, synchronized, this, throw, throws, transient, try, void, volatile, while  
// record, sealed, permits, non-sealed, var, yield

// *Note: const and goto are reserved but not used.

// 2. Identifiers
// ----------------
// Custom names for classes, interfaces, methods, variables, parameters, and labels.

// Rules:
//   - Must start with a letter, underscore (_), or dollar sign ($).
//   - Must not be a keyword.
//   - Can be of any length.

// Example:
//   int age;  
//   String firstName;

// 3. Literals
// ------------
// Constant values assigned to variables.

// Types:
//   ● Integer literals: 123, 0x1A, 077, 0b1010  
//   ● Floating-point literals: 3.14, 2.0e10, 1.0f, 2.5d  
//   ● Character literals: 'A', '\n', '\u0041'  
//   ● String literals: "Hello", "Java\n"  
//   ● Boolean literals: true, false  
//   ● Null literal: null

// 4. Operators
// ------------
// Symbols that perform operations.

// ➤ Arithmetic Operators:
//     +, -, *, /, %

// ➤ Assignment Operators:
//     =, +=, -=, *=, /=, %=, &=, |=, ^=, <<=, >>=, >>>=

// ➤ Relational Operators:
//     ==, !=, >, <, >=, <=

// ➤ Logical Operators:
//     &&, ||, !

// ➤ Bitwise Operators:
//     &, |, ^, ~, <<, >>, >>>

// ➤ Unary Operators:
//     +, -, ++, --, !

// ➤ Ternary Operator:
//     ? :

// ➤ instanceof:
//     instanceof

// ➤ Lambda Expression Operator (Java 8+):
//     ->

// ➤ Method Reference Operator (Java 8+):
//     ::

// 5. Separators (Delimiters)
// --------------------------
// Used to group or separate code elements.

//   • Parentheses: (  )
//   • Braces: {  }
//   • Brackets: [  ]
//   • Semicolon: ;
//   • Comma: ,
//   • Dot: .
//   • Ellipsis (for varargs): ...
//   • Annotation Marker: @
//   • Method Reference (also a separator): ::

// ------------------------------------------------------------

