public class UnicodeExample {
    public static void main(String[] args) {
        // Unicode character for 'அ' (Tamil letter A) is U+0B85
        char tamilChar = '\u0B85';
        System.out.println("Tamil Character: " + tamilChar);

        // Unicode character for '✓' (check mark) is U+2713
        String checkMark = "\u2713";
        System.out.println("Check Mark: " + checkMark);
    }
}
